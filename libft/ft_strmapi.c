#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		len;
	char	*tab;

	i = 0;
	if (!s || !f)
		return (0);
	len = ft_strlen(s);
	tab = malloc((len + 1) * sizeof(char));
	if (!tab)
		return (0);
	while (s[i])
	{
		tab[i] = f(i, s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
